package vo;

public class CityDTO {

	private	int 	cityNo;
	private	String 	city;
	private	String	cityEgname;
	private	String	cityCode;
	private	String	cityCountry;
	private	String	citycount;
	
	
	public int getCityNo() {
		return cityNo;
	}
	public void setCityNo(int cityNo) {
		this.cityNo = cityNo;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getCityEgname() {
		return cityEgname;
	}
	public void setCityEgname(String cityEgname) {
		this.cityEgname = cityEgname;
	}
	public String getCityCode() {
		return cityCode;
	}
	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}
	public String getCityCountry() {
		return cityCountry;
	}
	public void setCityCountry(String cityCountry) {
		this.cityCountry = cityCountry;
	}
	public String getCitycount() {
		return citycount;
	}
	public void setCitycount(String citycount) {
		this.citycount = citycount;
	}





}
